            <p>Paul Hood is digital director at <a href="http://www.archant.co.uk/" title="Archant" target="blank">Archant</a>, one of the U.K.’s largest independent media businesses, active in the fields of regional news, magazines and websites. He knows better than anyone that competition in the news media space where Archant operates is particularly fierce. Using London as an example, at least five prominent news media brands in the city are dedicated to delivering news and sports information to a London-centric audience.</p>
            <p>“Across the London region, Archant publishes 16 newspaper titles, each with its own website,” Hood says. “The question we asked ourselves was, ‘How can we leverage our high-quality local coverage of London’s biggest news and sports stories and bring them to a wider pan-London audience?’ Our answer was to create a new, digital-only news media brand and focus on addressing the gap in the market.</p>
            <p>“The result was <a href="http://www.london24.com/" title="London24" target="blank">London24</a>, a London-centric news media proposition produced exclusively by knowledgeable, local, London-based journalists. Local freesheets do a great job of catering to the commuter — the top national and London news stories are well-packaged and presented to give a good overview. But there was no news media brand covering London from the local angle.”</p>
