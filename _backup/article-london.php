                    <h3>Bringing It All Back Home</h3>
                    <p>Innovative advertising is only as good as the viewers who can see it, and that’s where audiences are key. Local media publishers can really benefit from innovative advertising; they’re the ones with strong community audiences. Larger national newspaper titles have spent the past five years struggling with pay walls — an issue that just doesn’t exist in the local news landscape.</p>
