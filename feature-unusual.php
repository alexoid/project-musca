            <h3>A literary horizon</h3>

            <p>
                <img src="_assets/unusual/sicha-balk.jpg" class="sicha-balk"/>

                "My job as an editorial person has changed a lot in the past two years," says Choire Sicha, editor
                of <a href="http://www.theawl.com/" title="The Awl" target="blank">The Awl</a>. "Two years ago,
                people didn’t know they wanted more satisfying stories online." In an experiment to offer an
                alternative to "drive-by-blogging," Sicha and Alex Balk, both <a href="http://gawker.com/"
                                                                                 title="Gawker"
                                                                                 target="blank">Gawker</a>
                and <a href="http://radaronline.com/" title="Radar" target="blank">Radar</a> alums, launched The Awl
                (tagline: Be Less Stupid) in 2009. The site offers readers about 15 posts a day — ranging from
                hand-drawn infographics to personal essays to poems — authored primarily by outside contributors.
                Few topics are typical, but the content is instead united by a sense of offbeat identity that
                appeals to vaguely hip 20- and 30-somethings with a taste for irony and pop culture. </p>

            <p>"We’ve always wanted to be the really attractive, shiny thing in the corner. None of us have ever
                been good at self-promotion, even in reasonable ways, but people love to discover on the Web," Sicha
                says. "I want people to find us organically. It makes for a coherent and unified readership."</p>
