            <h3></h3>


            <p>
                <img src="_assets/unusual/simmons.jpg" class="simmons"/>

                It’s a similar attitude that comes from many other digital platforms for creative prose and
                long-form journalism. <a href="http://therumpus.net/" title="The Rumpus" target="blank">The
                    Rumpus</a>, <a href="http://thenewinquiry.com/" title="The New Inquiry" target="blank">The New
                    Inquiry</a> and <a href="http://www.mcsweeneys.net/tendency"
                                       title="McSweeney’s Internet Tendency" target="blank">McSweeney’s Internet
                    Tendency</a> have given creative writers and their fans new outlets. They mix smart commentary
                on pop culture, humor and essays to create a well-honed sense of editorial style. As well, ESPN’s
                Bill Simmons spin-off <a href="http://www.grantland.com/" title="Grantland"
                                         target="blank">Grantland</a> has created a new platform for top-notch
                sportswriting. </p>

            <p>Readership of The Awl swelled to about half a million visitors by the fall of 2010. Eventually, the
                site was making enough money to support its staff and spin-off sites devoted to women’s interests
                (<a href="http://thehairpin.com/" title="The Hairpin" target="blank">The Hairpin</a>), money (<a
                    href="http://thebillfold.com/" title="The Billfold" target="blank">The Billfold</a>), comedy (<a
                    href="http://splitsider.com/" title="Splitsider" target="blank">Splitsider</a>) and gadgets (<a
                    href="http://thewirecutter.com/" title="The Wirecutter" target="blank">The Wirecutter</a>) have
                launched since. "We think whatever happens from here on out is gravy," Sicha says. "Largely, we’re
                surprised it’s been around this long already. We didn’t have a business strategy."</p>

            <p>Being a nimble and relatively niche site allowed The Awl some wiggle room when it came to making
                money. With clearly distinguishable readers and a knack for catering content for them, The Awl has
                created advertising products that mesh relatively seamlessly with the rest of the site’s content —
                an approach that serves both readers and advertisers. They created a commercial for Newcastle Brown
                Ale that was cross-promoted on posts and a <a href="http://splitsider.com/2011/12/hitchin-a-ride-1/"
                                                              title="video series sponsored by Ford" target="blank">video
                    series sponsored by Ford</a> that featured a Saturday Night Live cast member driving his comedic
                friends around. </p>

            <p>Out of the rise of mobile and creative moneymaking has come a trend of micropublishing. <a
                    href="https://www.byliner.com/" title="Byliner" target="blank">Byliner</a>, a consortium of
                well-known editors and writers, publishes articles and essays that cost a few dollars each. If you
                want to read, you have to pay. <a href="https://www.atavist.com/" title="Atavist"
                                                  target="blank">Atavist</a>,
                a digital platform as well as publisher, also asks readers to fork over a couple of dollars for a
                good, long read available only through e-readers or the Atavist app. </p>
        </div>
    </div>
</section>

