            <h3>Storybook lives</h3>

            <p>
                <img src="_assets/unusual/todd-selby.jpg" class="tod-selby"/>

                For Todd Selby, building a site was just a hobby, an extension of his day job as a professional
                photographer specializing in portraits and fashion photography for magazines and advertisers.
                Growing restless waiting for assignments and inspired by the living spaces of his creative friends
                and assigned subjects, he launched <a href="http://theselby.com/" title="The Selby" target="blank">The
                    Selby</a> in 2008.</p>

            <p>The conceit is simple: gorgeous photographs of supremely interesting interiors. But the result is
                striking. With images of knickknack collections, stacked art supplies and intimate portraits of
                people in their homes — as well as a hand-drawn questionnaire denouement to each post — The Selby
                manages to capture the spirit of a person's life with objects. "I just really wanted to do this cool
                thing and put it on the Internet," Selby says. "Working online is the way to do things. It's global,
                and it's instantaneous, and it's forever."</p>

            <p>The site has been an unparalleled calling card for Selby, who supports himself by working on
                advertising collaborations. He has produced a <a href="http://louisvuittonexpress.com/"
                                                                 title="Louis Vuitton" target="blank">series of
                    travel journals for Louis Vuitton</a>, a <a
                    href="http://theselby.com/galleries/collaborations/the-selby-x-fendi/"
                    title="portfolio of photographs for Fendi" target="blank">portfolio of photographs for Fendi</a>
                and a <a href="http://www.youtube.com/watch?feature=player_embedded&v=s3gDnnTfE2E"
                         title="branding video for Zara" target="blank">branding video for Zara</a>. His inimitable
                style also has filled two books, <a href="http://www.amazon.com/Selby-Your-Place-Todd/dp/0810984865"
                                                    title="The Selby Is in Your Place" target="blank">The Selby Is
                    in Your Place</a> and most recently <a
                    href="http://www.amazon.com/Edible-Selby-Todd/dp/0810998041/ref=sr_1_1?s=books&ie=UTF8&qid=1361397656&sr=1-1&keywords=Edible+Selby"
                    title="Edible Selby" target="blank">Edible Selby</a>, a photo collection of the culinary
                workspaces and restaurants of leading chefs that sprouted from a column Selby began doing in 2011
                for <em>The New York Times'</em> quarterly <a
                    href="http://www.nytimes.com/pages/t-magazine/index.html" title="T Magazine" target="blank"><em>T
                        Magazine</em></a>.</p>

            <p>Several other people are creating interesting spaces online from which to show off their work. <a
                    href="http://www.freundevonfreunden.com/" title="Freunde von Freunden" target="blank">Freunde
                    von Freunden</a>, founded by a German creative trio, interviews interesting people and showcases
                their living spaces around the globe. As well, <a href="http://www.thecoolhunter.net/"
                                                                  title="The Cool Hunter" target="blank">The Cool
                    Hunter</a>, founded by Bill Tikos, spotlights posh homes, hotels, restaurants and objects in a
                narrative format. In the style realm, the rise of street style is undeniable. Unlike the style
                bloggers documenting personal outfits, bloggers such as Scott Schuman of <a
                    href="http://www.thesartorialist.com/" title="The Sartorialist" target="blank">The
                    Sartorialist</a>, Tommy Ton of <a href="http://jakandjil.com/" title="Jak & Jil" target="blank">Jak
                    & Jil</a>, Phil Oh of <a href="http://streetpeeper.com/" title="Street Peeper" target="blank">Street
                    Peeper</a> and <a href="http://www.garancedore.fr/en/" title="Garance Doré" target="blank">Garance
                    Doré</a> of her eponymous site have turned their lenses on the sophisticated public to
                celebrated effect. They snag images of the world’s most fashionable people as well as everyday
                trendsetters. Picture by picture, they are telling the visual story of the world’s most coveted
                streets and buildings.</p>
        </div>
    </div>
</section>

