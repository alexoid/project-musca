            <h3></h3>
            <p>Meanwhile, long-form writing is having a bit of resurgence online via smaller publishers. Many have
                used <a href="http://www.kickstarter.com/" title="Kickstarter" target="blank">Kickstarter</a>
                campaigns to raise initial funds and are staffed by seasoned journalists. <a
                    href="https://www.readmatter.com/" title="MATTER" target="blank">MATTER</a> collected nearly
                three times its fundraising goal of $50,000 and published its first story (available for $.99) in
                November. <a href="http://narrative.ly/" title="Narratively" target="blank">Narratively</a>, funded
                by a $54,000 Kickstarter haul, publishes a new story about New York City every day. But the most
                heralded publication is probably <a href="http://www.tomorrowthemag.com/" title="Tomorrow"
                                                    target="blank">Tomorrow</a>, a one-off magazine founded by the
                eight <a href="http://www.good.is/everyone" title="Good Magazine" target="blank">Good Magazine</a>
                staff members who were laid off in June. </p>

            <p>The former Good-ers were determined to turn the turn of fate into inspiration for something of
                creative substance. In less than three weeks, they hatched an idea for a new publication, raised
                enough money to finance it and were digging through hundreds of pitches from willing writers and
                offers to help them design the issue and fact-check the features.</p>

            <p>The process was apparently simple. After being unceremoniously canned, the staffers asked for $15,000
                via a Kickstarter campaign. Less than five hours later, they met their fundraising goal. Of course,
                it helped that several of the staffers were relatively well-known, with large Twitter, Tumblr and
                industry followings thanks to their previous work. "I think people supported the project because
                they supported us. The content was an ancillary benefit," says editor Ann Friedman. A total of 1,779
                people contributed $45,452 during the month-long campaign.</p>

            <p>The relative success of the project has sbeen celebrated as a beacon and an example of the sort of
                future that's possible for storytellers in the digital age. But the reality is a little more
                complicated — Friedman says they ended up breaking even; to make a profit that would allow the
                project to be sustainable, they would have had to raise at least eight times their initial
                fundraising goal.</p>

            <p>There’s no guarantee that what works now will work in the future or that brilliant ideas can be
                sustainable. Internet video is rumored to be the next big thing, native advertising promises to
                revive a sagging digital economy, and storytellers will continue to be challenged with
                entrepreneurialism. "I don’t think there’s one solution. That’s precisely why it’s exciting,"
                Friedman says. "What you’re seeing is a lot of possibilities."</p>
