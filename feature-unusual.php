            <h3>Step-by-Step</h3>


            <p>Another thriving facet of digital storytelling is more explicitly instructive. How to remodel your
                house. How to make a gourmet meal in a shoebox kitchen. How to build a holiday centerpiece and make
                homemade wood cleaner. In other words, there's a Martha Stewart for everyone and every ambition.</p>



            <p>
                <img src="_assets/unusual/pippa-lord.jpg" class="pippa-lord"/>

                Last year, Pippa Lord launched <a href="http://sousstyle.com/" target="blank" title="Sous Style">Sous
                    Style</a>, a site half-devoted to showing how beautiful people live through gorgeous photographs
                and breezy questionnaires and half-devoted to passing on recipes, dining hotspots and housewarming
                tricks. Lord, a photo editor at <a href="http://www.elle.com/" title="ELLE" target="blank">ELLE</a>,
                lists the site as designed for a "new generation of homemakers" that comes replete with a manifesto.
            </p>

            <p>For example: "Our ‘houses’ might be apartments or rooms we rent, but they are spaces we call our
                own." She’s not alone in trying to perfect the quirky art of urban homemaking. <a
                    href="http://smittenkitchen.com/" title="Smitten Kitchen" target="blank">Smitten Kitchen</a>, a
                recipe blog run by Deb Perelman is an account of "fearless cooking from a tiny kitchen in New York
                City." From the West Coast, Joy Wilson of <a href="http://joythebaker.com/" title="Joy the Baker"
                                                             target="blank">Joy the Baker</a>, is a self-taught
                foodie with a sweet tooth, who stuffs her recipes amid humorous life bits and pictures of her cat.
                As well, Emily Schuman of Los Angeles is the creator of the hugely popular site <a
                    href="http://cupcakesandcashmere.com/" title="Cupcakes and Cashmere" target="blank">Cupcakes and
                    Cashmere</a>, which details her exploits at creating a glamorous life, from closet to kitchen.
            </p>


